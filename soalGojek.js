    while( filterData.length <= 100 && params.length != 0) {
        temp = params.slice(0,3);
        let A = temp[0];
        let B = temp[1];
        let K = temp[2];

        if (A <= B && B < 10000 && B >= K) {
            params.splice(0, 3)
            filterData.push(temp);
        } else {
            params.shift();
        }
    }

